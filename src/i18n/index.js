// ── 便捷取值函数，在组件里用 t('market.title') ───────────────────────────
export function getText(lang, path) {
  const keys = path.split('.')
  let node = i18n
  for (const k of keys) {
    if (node == null) return path
    node = node[k]
  }
  if (node == null) return path
  if (typeof node === 'object' && (node.zh !== undefined || node.en !== undefined)) {
    return node[lang] ?? node.zh ?? path
  }
  return node
}
